

def celsius():
    celsius = float(input("Enter Temperature in celsius: "))
    f = celsius * (9.0/5.0) + 32
    print(f)
celsius()

def farenheit():
    farenheit = float(input("Enter Temperature in Farenheit: "))
    c = (farenheit - 32) * (5.0/9.0)
    print(c)
farenheit()

     




